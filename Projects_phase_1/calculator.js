// basic calculator that can perform addition, subtraction, multiplication, and division
function calculator(operation, n1, n2){
    switch(operation){
        case "+":
            return n1 + n2;
        case "-":
            return n1 - n2;
        case "*":
            return n1 - n2;
        case "/":
            return n1 - n2;
    };
};

console.log(calculator("-", 5, 3));